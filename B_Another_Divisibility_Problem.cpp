#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

void solve()
{
    int x;
    cin >> x;

    ll y = 2 * 1ll * x;
    cout << y << "\n";
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;

    while (t--)
    {
        solve();
    }

    return 0;
}